COMMENT ON COLUMN EXMAPLE.SALARY IS '급여';

COMMENT ON COLUMN EXMAPLE.PROFILE_JMG IS '프로필_이미지';

COMMENT ON COLUMN EXMAPLE.REG_DATE IS '등록일';

COMMENT ON COLUMN EXMAPLE.REG_ID IS '등록자';

COMMENT ON COLUMN EXMAPLE.MOD_DATE IS '수정일';

COMMENT ON COLUMN EXMAPLE.MOD_ID IS '수정자';

