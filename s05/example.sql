/* 테이블 설계 예시 */
CREATE TABLE EXMAPLE (
	ID NUMBER(5) NOT NULL, /* 아이디 */
	NAME VARCHAR2(8) NOT NULL, /* 이름 */
	BIRTH_DATE DATE, /* 생년월일 */
	SALARY NUMBER(9) DEFAULT 0, /* 급여 */
	PROFILE_JMG BLOB, /* 프로필_이미지 */
	REG_DATE DATE DEFAULT SYSDATE, /* 등록일 */
	REG_ID NUMBER(5), /* 등록자 */
	MOD_DATE DATE DEFAULT SYSDATE, /* 수정일 */
	MOD_ID NUMBER(5) /* 수정자 */
);

